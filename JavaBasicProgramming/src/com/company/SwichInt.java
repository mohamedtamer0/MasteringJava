package com.company;

public class SwichInt {
    public static void main(String[] args) {
        int number = 1;
        switch (number) {
            case 1:
                System.out.println("This is No1");
                break;
            case 2:
                System.out.println("This is No2");
                break;
            case 3:
                System.out.println("This is No3");
                break;
            default:
                break;
        }
    }
}
